// Declare boolean variables
let isRaining = true;
let isSunny = false;

// Perform boolean operations
let isRainyAndSunny = isRaining && isSunny; // Logical AND
let isRainyOrSunny = isRaining || isSunny; // Logical OR
let isNotRaining = !isRaining; // Logical NOT

// Display the results
console.log("Is it raining and sunny?", isRainyAndSunny);
console.log("Is it raining or sunny?", isRainyOrSunny);
console.log("Is it not raining?", isNotRaining);
