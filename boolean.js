// Declare a boolean variable
var isTrue = true;
var isFalse = false;

// Use boolean variables in conditional statements
if (isTrue) {
  console.log("isTrue is true");
} else {
  console.log("isTrue is false");
}

if (isFalse) {
  console.log("isFalse is true");
} else {
  console.log("isFalse is false");
}
