// Declare a variable and initialize it with null
let myNullValue = null;

// Check if a variable has a null value
if (myNullValue === null) {
  console.log("myNullValue is null");
} else {
  console.log("myNullValue is not null");
}

// Use null to represent the absence of a value
let missingValue = null;

if (missingValue === null) {
  console.log("The value is missing");
} else {
  console.log("The value is present");
}
