// Function that calculates the square of a number and returns the result
function calculateSquare(number) {
    var square = number * number;
    return square; // Using the return keyword to specify the value to be returned
  }
  
  // Call the function and store the result in a variable
  var result = calculateSquare(5);
  
  // Display the result
  console.log("The square of 5 is: " + result);
  